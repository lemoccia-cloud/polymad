def get_text(key: str, lang: str = "en") -> str:
    """Return translated string for key in given language, fallback to English."""
    lang = lang if lang in TRANSLATIONS else "en"
    return TRANSLATIONS[lang].get(key, TRANSLATIONS["en"].get(key, key))


def format_number(n: float, lang: str = "en", decimals: int = 2) -> str:
    """Format a number with locale-appropriate separators."""
    if lang in ("pt", "es"):
        # Use comma as decimal separator, dot as thousands
        formatted = f"{n:,.{decimals}f}"
        # swap . and ,
        formatted = formatted.replace(",", "X").replace(".", ",").replace("X", ".")
        return formatted
    return f"{n:,.{decimals}f}"


def format_currency(n: float, lang: str = "en") -> str:
    if lang in ("pt", "es"):
        return f"$ {format_number(n, lang, 2)}"
    if lang == "zh":
        return f"$ {format_number(n, lang, 2)}"
    return f"${format_number(n, lang, 2)}"


def format_pct(n: float, lang: str = "en", decimals: int = 1) -> str:
    """Format a fraction (0-1) as a percentage string."""
    val = n * 100
    if lang in ("pt", "es"):
        return f"{format_number(val, lang, decimals)}%"
    return f"{val:.{decimals}f}%"


def format_date(dt: "datetime", lang: str = "en") -> str:
    """Format a datetime for display."""
    if lang == "pt":
        return dt.strftime("%d/%m/%Y")
    if lang == "es":
        return dt.strftime("%d/%m/%Y")
    if lang == "zh":
        return dt.strftime("%Y年%m月%d日")
    return dt.strftime("%b %d, %Y")
